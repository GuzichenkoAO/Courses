package com.guzichenko.courses.lesson_7;

public class A {

    public int i;
    public int j;

    public A(int i, int j) {
        this.i = i;
        this.j = j;
    }

    @Override
    public String toString() {
        return "A{" +
                "i=" + i +
                ", j=" + j +
                '}';
    }
}
