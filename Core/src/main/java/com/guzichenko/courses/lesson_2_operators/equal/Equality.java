package com.guzichenko.courses.lesson_2_operators.equal;

public class Equality {

	public static void main(String[] args) {
		int i = 0;

		System.out.println("i == 2: " + (i == 2));
		System.out.println("i != 2: " + (i != 2));
		System.out.println("i > 2: " + (i > 2));
		System.out.println("i >= 2: " + (i >= 2));
		System.out.println("i < 2: " + (i < 2));
		System.out.println("i <= 2: " + (i <= 2));

	}

}
