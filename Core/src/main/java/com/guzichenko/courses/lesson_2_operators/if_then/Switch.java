package com.guzichenko.courses.lesson_2_operators.if_then;

public class Switch {

	public static void main(String[] args) {
		int i = 2;

		switch (i) {

			case 2:
				System.out.println(2);
				break;
			case 3:
				System.out.println(3);
				break;
			case 6:
				System.out.println(6);
				break;
			default:
				System.out.println("default");
		}

	}

}
